package chapter_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); //true
        System.out.println(5 >= 3); //tr
        System.out.println(5 >= 5); //tr
        System.out.println(5 >= 7); //false

        System.out.println(5 == 5); //tr
        System.out.println(5 != 3); //tr

    }
}
